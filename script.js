function renderResults() {
  const featured = $("#featuredResultsGrid");
  if (featured) {
    featured.innerHTML = RESULTS.slice(0, 2).map(resultCard).join("");
  }

  const full = $("#resultsGrid");
  if (full) {
    let list = [...RESULTS];

    const active = $(".filter-btn.active")?.dataset.filter || "All";
    const q = ($("#resultSearch")?.value || "").toLowerCase();

    if (active !== "All") {
      list = list.filter((r) => r.category === active);
    }

    if (q) {
      list = list.filter((r) => JSON.stringify(r).toLowerCase().includes(q));
    }

    const sort = $("#sortResults")?.value || "default";

    if (sort === "school") {
      list.sort((a, b) => langField(a, "school").localeCompare(langField(b, "school")));
    }

    if (sort === "category") {
      list.sort((a, b) => a.category.localeCompare(b.category));
    }

    full.innerHTML = list.map(resultCard).join("") || `
      <div class="empty-state">
        ${currentLang === "vi" ? "Không tìm thấy kết quả phù hợp." : "No matching results found."}
      </div>
    `;
  }

  bindResultButtons();
  observeReveals();
}

