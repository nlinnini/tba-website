let currentLang = localStorage.getItem("tbaLang") || "en";

const $ = (s, root = document) => root.querySelector(s);
const $$ = (s, root = document) => [...root.querySelectorAll(s)];

function t(key) {
  return I18N[currentLang][key] || I18N.en[key] || key;
}

function langField(obj, key) {
  return obj[key + (currentLang === "vi" ? "VI" : "EN")] || obj[key + "EN"] || "";
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("tbaLang", lang);
  document.documentElement.lang = lang;

  $$("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });

  $$(".lang-pill").forEach((p) => {
    p.classList.toggle("active", p.dataset.lang === lang);
  });

  renderAll();
}

function bindBasics() {
  $$(".lang-toggle").forEach((btn) => {
    btn.addEventListener("click", (e) => {
      const pill = e.target.closest(".lang-pill");
      if (pill) setLang(pill.dataset.lang);
    });
  });

  $(".menu-button")?.addEventListener("click", () => {
    $(".mobile-menu")?.classList.toggle("open");
  });

  $$(".book-link").forEach((a) => {
    a.href = BOOKING_LINK;
  });

  const email = $("#emailLink");
  if (email) {
    email.href = `mailto:${EMAIL}?subject=Free consultation inquiry`;
  }

  const glow = $("#cursorGlow");
  document.addEventListener("pointermove", (e) => {
    if (glow) {
      glow.style.left = e.clientX + "px";
      glow.style.top = e.clientY + "px";
    }
  });
}

